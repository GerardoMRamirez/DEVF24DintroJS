// operador logico "AND". todas las condiciones se tienen que cumplir
if (4 == "4" && 5 === 5 ) {
    console.log("la condicion se cumplio AND");
} else {
    console.log("la condicion no se cumplio AND");
}

// operador logico "OR". una u otra condicion se tiene que cumplir

if (4 === "4" || 5 === 5 ) {
    console.log("la condicion se cumplio OR");
} else {
    console.log("la condicion no se cumplio OR");
}

